/*CREATING TABLE AUTHOR*/
CREATE TABLE AUTHOR(AUTHOR_ID VARCHAR(5) PRIMARY KEY , LASTNAME VARCHAR(15) NOT NULL, FIRSTNAME VARCHAR(15) NOT NULL, EMAIL VARCHAR(40), CITY VARCHAR(15), COUNTRY VARCHAR(15));
/*CREATING TABLE BOOK*/
CREATE TABLE BOOK(BOOK_ID VARCHAR(5) PRIMARY KEY CHECK(BOOK_ID LIKE 'B%'), BOOK_TITLE VARCHAR(30) NOT NULL, COPIES INT CHECK(COPIES>2));
/*CREATING TABLE AUTHOR_LIST*/
CREATE TABLE AUTHOR_LIST(AUTHOR_ID VARCHAR(5) REFERENCES AUTHOR(AUTHOR_ID), BOOK_ID VARCHAR(5) REFERENCES BOOK(BOOK_ID), ROLE VARCHAR(15), PRIMARY KEY(AUTHOR_ID,BOOK_ID));
